// Numbers in javascript

var num1 = 10;
// To increment by 1 a++ is a = a + 1
num1++;
console.log(num1);

// To decrement by 1 a-- is a = a - 1
num1--;
console.log(num1);

// Divide multiply reminder
console.log(num1 / 6);  // Gives value as 1.6666666666666667 with decimal number

console.log(30 % 6);  // Gives value as 0 a reminder

// To increment by more than 1
num1 += 10;
console.log(num1);

// // Math.floor will round down the decial value
console.log(Math.floor(2.8)); //output: 2
// Math.floor will round up the decial value
console.log(Math.round(2.6)); //output: 3
// To generat random number
console.log(Math.floor(Math.random())); //output: by default give value bt 0 to 1

console.log(Math.floor(Math.random() * 3)); //output: give value bt 0 to 2